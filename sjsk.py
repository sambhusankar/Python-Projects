import turtle as t
wn=t.getscreen()
t.color('red','green',)
def work(x,y):
    
    
        
    t.up()
        
    t.goto(x,y)
    t.down()
    t.begin_fill()
    for I in range(5):
            
         
         
         t.lt(144)
         t.fd(100)
    t.end_fill()
    
       
wn.onclick(work)

t.done()